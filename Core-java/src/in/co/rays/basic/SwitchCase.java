package in.co.rays.basic;

public class SwitchCase {

	public static void main(String[] args) {

		int number = 2;

		switch (number) {
		case 1:
			System.out.println("One");

		case 2:
			System.out.println("Tow");

			break;

		default:

			System.out.println(" Invalid Number ");
			break;
		}

	}

}
