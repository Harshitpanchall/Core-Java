package in.co.rays.basic.exercise.java;

public class MaxMini {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		
		if(a>b) {
			System.out.println("max"+ a);
			
		}else {
			System.out.println("max"+ b);
			
		}
		if(a<b) {
			System.out.println("mini" + a);
			
		} else {
			System.out.println("mini" + b);
			
		}

	}

}
